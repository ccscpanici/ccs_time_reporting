def _save_generated_artifact(timesheet, generated_path, created_by, export_format):
    generated_path = Path(generated_path)
    file_type = "xlsx" if generated_path.suffix.lower() == ".xlsx" else "pdf"

    artifact = TimesheetSubmissionArtifact(
        timesheet=timesheet,
        file_type=file_type,
        export_format=export_format,
        created_by=created_by,
        submitted=True,
    )
    artifact.file.save(generated_path.name, ContentFile(generated_path.read_bytes()), save=True)
    return artifact


def _save_receipts_artifact(timesheet, receipts_bytes, filename, created_by):
    artifact = TimesheetSubmissionArtifact(
        timesheet=timesheet,
        file_type=TimesheetSubmissionArtifact.FileType.PDF,
        export_format=Timesheet.ExportFormat.PDF,
        created_by=created_by,
        submitted=True,
    )
    artifact.file.save(filename, ContentFile(receipts_bytes), save=True)
    return artifact


def send_timesheet_approved_email(timesheet, approved_by):
    """Generate approved timesheet attachments and email active approval recipients.

    Raises exceptions to the caller so the view can show a warning while keeping
    the approval itself successful.
    """
    config = EmailConfiguration.active_config()
    if not config:
        raise ValueError("No active email configuration exists.")

    recipients = list(
        ApprovalNotificationRecipient.objects.filter(active=True).values_list("email", flat=True)
    )
    if not recipients:
        raise ValueError("No active approval notification recipients exist.")

    timesheet = (
        Timesheet.objects
        .select_related("employee", "approved_by")
        .prefetch_related("receipts")
        .get(pk=timesheet.pk)
    )

    backend = _smtp_backend(config)
    subject = _approval_email_subject(timesheet)
    body = _approval_email_body(timesheet)

    message = EmailMessage(
        subject=subject,
        body=body,
        from_email=config.from_email,
        to=recipients,
        reply_to=[config.reply_to_email] if config.reply_to_email else None,
        connection=backend,
    )

    generated_attachments = []

    if timesheet.can_export_excel:
        excel_path = Path(build_timesheet_excel(timesheet))
        _save_generated_artifact(
            timesheet,
            excel_path,
            approved_by,
            Timesheet.ExportFormat.EXCEL,
        )
        generated_attachments.append((
            f"{_employee_initials(timesheet.employee)}_{timesheet.week_start:%Y%m%d}.xlsx",
            excel_path.read_bytes(),
            "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
        ))

    pdf_path = Path(build_timesheet_pdf(timesheet))
    _save_generated_artifact(
        timesheet,
        pdf_path,
        approved_by,
        Timesheet.ExportFormat.PDF,
    )
    generated_attachments.append((
        f"{_employee_initials(timesheet.employee)}_{timesheet.week_start:%Y%m%d}_timesheet.pdf",
        pdf_path.read_bytes(),
        "application/pdf",
    ))

    receipts_bytes = build_receipts_pdf_bytes(timesheet)
    receipts_filename = receipts_pdf_filename(timesheet)
    _save_receipts_artifact(timesheet, receipts_bytes, receipts_filename, approved_by)
    generated_attachments.append((receipts_filename, receipts_bytes, "application/pdf"))

    for filename, content, mimetype in generated_attachments:
        message.attach(filename, content, mimetype)

    message.send()
    logger.info("Sent approved timesheet email for timesheet %s to %s", timesheet.pk, recipients)



def send_employee_timesheet_approved_email(timesheet, approved_by):
    config = EmailConfiguration.active_config()
    if not config:
        raise ValueError("No active email configuration exists.")

    if not timesheet.employee.email:
        raise ValueError("Employee does not have an email address.")

    approver = approved_by.get_full_name() or approved_by.get_username()
    subject = f"Timesheet Approved - Week of {timesheet.week_start:%m/%d/%Y}"
    body = "\n".join([
        f"Your timesheet for the week of {timesheet.week_start:%m/%d/%Y} has been approved.",
        "",
        f"Approved By: {approver}",
        "",
        "View Timesheet:",
        _timesheet_url(timesheet),
    ])

    _send_basic_email(
        config=config,
        subject=subject,
        body=body,
        to=[timesheet.employee.email],
    )


