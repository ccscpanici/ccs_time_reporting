import logging
from pathlib import Path

from django.conf import settings
from django.core.files.base import ContentFile
from django.core.mail import EmailMessage
from django.core.mail.backends.smtp import EmailBackend
from django.utils import timezone

from ..models import (
    ApprovalNotificationRecipient,
    EmailConfiguration,
    Timesheet,
    TimesheetSubmissionArtifact,
)
from .exporter import build_timesheet_excel, build_timesheet_pdf
from .receipts_pdf import build_receipts_pdf_bytes, receipts_pdf_filename

logger = logging.getLogger(__name__)


def _employee_initials(user):
    full_name = user.get_full_name().strip()
    if full_name:
        return "".join(part[0].upper() for part in full_name.split() if part)
    return user.get_username()[:2].upper()


def _timesheet_url(timesheet):
    base_url = getattr(settings, "SITE_BASE_URL", "https://timetrack.ccswi.us").rstrip("/")
    return f"{base_url}{timesheet.get_absolute_url()}"


def _send_basic_email(*, config, subject, body, to):
    recipients = [email for email in to if email]
    if not recipients:
        return

    backend = _smtp_backend(config)
    message = EmailMessage(
        subject=subject,
        body=body,
        from_email=config.from_email,
        to=recipients,
        reply_to=[config.reply_to_email] if config.reply_to_email else None,
        connection=backend,
    )
    message.send()


def _approval_email_subject(timesheet):
    employee = timesheet.employee
    first_name = employee.first_name or ""
    last_name = employee.last_name or ""

    if not (first_name or last_name):
        name = employee.get_full_name() or employee.get_username()
    else:
        name = f"{first_name} {last_name}".strip()

    return f"{name} Timesheet {timesheet.week_start:%m/%d/%Y} - Time & Expense"


def _approval_email_body(timesheet):
    employee_name = timesheet.employee.get_full_name() or timesheet.employee.get_username()
    approved_by = timesheet.approved_by.get_full_name() if timesheet.approved_by else ""
    if timesheet.approved_by and not approved_by:
        approved_by = timesheet.approved_by.get_username()

    return "\n".join([
        "A timesheet has been approved.",
        "",
        f"Employee: {employee_name}",
        f"Week Start: {timesheet.week_start:%m/%d/%Y}",
        f"Approved By: {approved_by or '—'}",
        f"Approved At: {timezone.localtime(timesheet.approved_at):%m/%d/%Y %I:%M %p}" if timesheet.approved_at else "Approved At: —",
        "",
        "View Timesheet:",
        _timesheet_url(timesheet),
        "",
        "Attachments:",
        "- Excel Timesheet",
        "- PDF Timesheet",
        "- Receipts PDF",
    ])


def _smtp_backend(config):
    return EmailBackend(
        host=config.smtp_host,
        port=config.smtp_port,
        username=config.smtp_username or None,
        password=config.smtp_password or None,
        use_tls=config.use_tls,
        use_ssl=config.use_ssl,
        fail_silently=False,
    )


