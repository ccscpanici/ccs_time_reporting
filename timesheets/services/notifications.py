def send_timesheet_submitted_supervisor_email(timesheet, submitted_by):
    config = EmailConfiguration.active_config()
    if not config:
        raise ValueError("No active email configuration exists.")

    profile = getattr(timesheet.employee, "employee_profile", None)
    supervisor = getattr(profile, "supervisor", None) if profile else None

    if not supervisor:
        raise ValueError("Employee does not have a supervisor assigned.")

    if not supervisor.email:
        raise ValueError("Supervisor does not have an email address.")

    employee_name = timesheet.employee.get_full_name() or timesheet.employee.get_username()
    submitter = submitted_by.get_full_name() or submitted_by.get_username()

    subject = f"{employee_name} Timesheet {timesheet.week_start:%m/%d/%Y} - Submitted"
    body = "\n".join([
        f"{employee_name} has submitted a timesheet for approval.",
        "",
        f"Week Start: {timesheet.week_start:%m/%d/%Y}",
        f"Submitted By: {submitter}",
        "",
        "Review Timesheet:",
        _timesheet_url(timesheet),
    ])

    _send_basic_email(
        config=config,
        subject=subject,
        body=body,
        to=[supervisor.email],
    )

